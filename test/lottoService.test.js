test("transformMultiDimensionalDataToStringType ", async () => {
  const query = {
    39: [1, 1, 1, 1, 2, 2],
    40: [1, 1, 1, 2, 1, 2],
    41: [1, 1, 2, 1, 1, 2],
    42: [1, 2, 1, 1, 1, 2],
    43: [1, 1, 1, 2, 2, 1],
    44: [1, 1, 2, 1, 2, 1],
    45: [1, 2, 1, 1, 2, 1],
    46: [1, 1, 2, 2, 1, 1],
    47: [1, 2, 1, 2, 1, 1],
    48: [1, 2, 2, 1, 1, 1],
  };

  const data =
    await lottoEnginService.transformMultiDimensionalDataToStringType(query);
  expect(data).toEqual({
    39: " 1  1  1  1  2  2",
    40: " 1  1  1  2  1  2",
    41: " 1  1  2  1  1  2",
    42: " 1  2  1  1  1  2",
    43: " 1  1  1  2  2  1",
    44: " 1  1  2  1  2  1",
    45: " 1  2  1  1  2  1",
    46: " 1  1  2  2  1  1",
    47: " 1  2  1  2  1  1",
    48: " 1  2  2  1  1  1",
  });
});

